package com.ironman.tracker.valueHolder;

public class WeeklyCallValueHolder {
    public String name;
    public String number;
    public double percent;
    public int fr;

    public WeeklyCallValueHolder(String name, String number, double percent, int fr) {
        this.name = name;
        this.number = number;
        this.percent = percent;
        this.fr = fr;
    }
}
